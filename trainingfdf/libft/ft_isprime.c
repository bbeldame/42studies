#include "libft.h"

int	ft_isprime(int nbr)
{
	int i;

	i = 2;
	if (!nbr)
		return (0);
	while (i < nbr)
	{
		if (nbr % i == 0)
			return (0);
		i++;
	}
	return (1);
}
