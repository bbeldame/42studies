#include "libft.h"

int		ft_int_max(int *tab)
{
	int i;
	int a;

	i = 0;
	a = tab[i];
	if (!tab)
		return (0);
	while (tab[i])
	{
		if (a < tab[i])
		{
			a = tab[i];
			i = 0;
		}
		i++;
	}
	return (a);
}
